// Data type Example
public class ViDu {
	public static void main(String[] args) {
		String bookName = "Lap Trinh Java";
		int publishingYear = 2021;
		double price = 15.5;
		boolean available = true;
		char repositoryCode = 'M';
		
		System.out.println("Book's Name: " + bookName);
		System.out.println("Year of publication: " + publishingYear);
		System.out.println("Price: " + price);
		System.out.println("Available: " + available);
		System.out.println("Repository Code: " + repositoryCode);
	}
}
